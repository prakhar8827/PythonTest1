# Prin week number based on week name
weekname='sunday'

if weekname=='sunday':
    print(1)
elif weekname == 'monday':
    print(2)
elif weekname == 'tuesday':
    print(3)
elif weekname == 'wednesday':
    print(4)
elif weekname == 'thursday':
    print(5)
elif weekname == 'friday':
    print(6)
elif weekname == 'saturday':
    print(7)
else:
    print("Invalid week number")

















